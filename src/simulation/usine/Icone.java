package simulation.usine;
 public class Icone {
        private String type;
        private String path;
    
        public Icone(String type, String path) {
            this.type = type;
            this.path = path;
        }
    
        public String getType() {
            return this.type;
        }
    
        public String getPath() {
            return this.path;
        }
    }

